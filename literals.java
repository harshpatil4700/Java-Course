public class literals {
    public static void main(String[] args) {
        byte age = 20;                   // integer literal 
        int age1 = 21;                   // integer literal
        short age2 = 22;                 // integer literal
        long phoneno = 981234567;        // integer literal : postfix of integers must be l or L 
        long phoneno1 = 981234567l;      // integer literal : postfix of integers must be l or L 
        char ch = 'h';                   // character literal
        float percentage = 91.1f;        // float literal : postfix of integers must be F or f
        double marks1 = 20.1;            // double literal
        double marks2 = 20.1d;           // double literal : postfix of integers must be d or D
        boolean a = true;                // boolean literal
        String name = "harsh";           // string literal : 'S' must be uppercase in string keyword

        System.out.println(age);
        System.out.println(age1);
        System.out.println(age2);
        System.out.println(phoneno);
        System.out.println(phoneno1);
        System.out.println(ch);
        System.out.println(percentage);
        System.out.println(marks1);
        System.out.println(marks2);
        System.out.println(a);
        System.out.println(name);
    }
}
