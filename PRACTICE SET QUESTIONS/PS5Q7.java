public class PS5Q7 {
    public static void main(String[] args) {
        int n=4;
        int i=n;
        int j =0;
        while(i>=0){
            //int j =0;
            while(j<=i){
                System.out.print("*");
                j++;
            }
            System.out.print("\n");
            i--;
        }
    }
}

