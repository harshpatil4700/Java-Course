public class print_function_using_println_and_print {
    public static void main(String[] args){
        System.out.println("hello world");           // using println i.e print next data to next line
        System.out.print("hello world ");             // using print i.e print next data after itself
        System.out.print("welcome to java");
        System.out.println("tit" + " " + "for" + " " + "tat");    // to print multiple string in one single statement we use + symbol
    }
}